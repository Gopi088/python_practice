

name = "Gopal"
age = 22
numbers = [10, 20, 30, 40]
city = "Bangalore"

# Equality and Inequality with Strings
print("Is name == 'Gopal'? I predict True.")
print(name == "Gopal")

print("\nIs name != 'Rahul'? I predict True.")
print(name != "Rahul")

print("\nIs city == 'Mumbai'? I predict False.")
print(city == "Mumbai")

# Using lower() Function
print("\nIs name.lower() == 'gopal'? I predict True.")
print(name.lower() == "gopal")

print("\nIs city.lower() == 'delhi'? I predict False.")
print(city.lower() == "delhi")

# Numerical Tests
print("\nIs age == 22? I predict True.")
print(age == 22)

print("\nIs age != 25? I predict True.")
print(age != 25)

print("\nIs age > 18? I predict True.")
print(age > 18)

print("\nIs age < 18? I predict False.")
print(age < 18)

print("\nIs age >= 22? I predict True.")
print(age >= 22)

print("\nIs age <= 20? I predict False.")
print(age <= 20)

# Using and Keyword
print("\nIs age > 18 and city == 'Bangalore'? I predict True.")
print(age > 18 and city == "Bangalore")

print("\nIs age > 25 and city == 'Bangalore'? I predict False.")
print(age > 25 and city == "Bangalore")

# Using or Keyword
print("\nIs age > 25 or city == 'Bangalore'? I predict True.")
print(age > 25 or city == "Bangalore")

print("\nIs age < 18 or city == 'Mumbai'? I predict False.")
print(age < 18 or city == "Mumbai")

# Item in List
print("\nIs 20 in numbers? I predict True.")
print(20 in numbers)

print("\nIs 50 in numbers? I predict False.")
print(50 in numbers)

# Item Not in List
print("\nIs 60 not in numbers? I predict True.")
print(60 not in numbers)

print("\nIs 30 not in numbers? I predict False.")
print(30 not in numbers)
